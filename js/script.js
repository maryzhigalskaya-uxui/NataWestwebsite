// Current language
let currentLanguage = 'pl';

// DOM elements
const applicationModal = document.getElementById('applicationModal');
const doneModal = document.getElementById('doneModal');
const mainCTAButton = document.querySelector('.button-1');
const contactForm = document.getElementById('contactForm');
const applicationForm = document.getElementById('applicationForm');
const langButtons = document.querySelectorAll('.lang-btn');
const closeButtons = document.querySelectorAll('.close');

// Initialize the page
document.addEventListener('DOMContentLoaded', function() {
    updateLanguage();
    setupEventListeners();
});

// Language switching
function updateLanguage() {
    const t = translations[currentLanguage];
    
    // Update all elements with data-key attributes
    document.querySelectorAll('[data-key]').forEach(element => {
        const key = element.getAttribute('data-key');
        const value = getNestedValue(t, key);
        if (value) {
            element.textContent = value;
        }
    });
    
    // Update placeholders
    document.querySelectorAll('[data-key-placeholder]').forEach(element => {
        const key = element.getAttribute('data-key-placeholder');
        const value = getNestedValue(t, key);
        if (value) {
            element.placeholder = value;
        }
    });
    
    // Update language button states
    langButtons.forEach(btn => {
        btn.classList.toggle('active', btn.getAttribute('data-lang') === currentLanguage);
    });
}

// Helper function to get nested object values
function getNestedValue(obj, path) {
    return path.split('.').reduce((current, key) => current && current[key], obj);
}

// Event listeners setup
function setupEventListeners() {
    // Language switching
    langButtons.forEach(button => {
        button.addEventListener('click', () => {
            currentLanguage = button.getAttribute('data-lang');
            updateLanguage();
        });
    });
    
    // Main CTA button - opens application modal
    mainCTAButton.addEventListener('click', () => {
        applicationModal.style.display = 'block';
        document.body.style.overflow = 'hidden';
    });
    
    // Contact form submission
    contactForm.addEventListener('submit', handleFormSubmission);
    applicationForm.addEventListener('submit', handleFormSubmission);
    
    // Modal close buttons
    closeButtons.forEach(button => {
        button.addEventListener('click', closeModal);
    });
    
    // Close modal when clicking outside
    window.addEventListener('click', (event) => {
        if (event.target === applicationModal || event.target === doneModal) {
            closeModal();
        }
    });
    
    // Close modal with ESC key
    document.addEventListener('keydown', (event) => {
        if (event.key === 'Escape') {
            closeModal();
        }
    });
}

// Form submission handler
function handleFormSubmission(event) {
    event.preventDefault();
    
    // Get form data
    const formData = new FormData(event.target);
    const data = Object.fromEntries(formData);
    
    // Validate form
    if (!validateForm(data)) {
        return;
    }
    
    // Simulate form submission
    submitForm(data).then(() => {
        // Close application modal if open
        applicationModal.style.display = 'none';
        
        // Show success modal
        doneModal.style.display = 'block';
        
        // Reset form
        event.target.reset();
    }).catch((error) => {
        console.error('Form submission error:', error);
        alert('Error submitting form. Please try again.');
    });
}

// Form validation
function validateForm(data) {
    const requiredFields = ['name', 'email', 'phone', 'service'];
    
    for (let field of requiredFields) {
        if (!data[field] || data[field].trim() === '') {
            alert(`Please fill in the ${field} field.`);
            return false;
        }
    }
    
    // Email validation
    const emailRegex = /^[^\s@]+@[^\s@]+\.[^\s@]+$/;
    if (!emailRegex.test(data.email)) {
        alert('Please enter a valid email address.');
        return false;
    }
    
    return true;
}

// Simulate form submission
function submitForm(data) {
    return new Promise((resolve, reject) => {
        // Create mailto link for form submission
        const subject = encodeURIComponent('Nowa prośba z formularza kontaktowego');
        const body = encodeURIComponent(`
Imię: ${data.name}
Email: ${data.email}
Telefon: ${data.phone}
Usługa: ${data.service}
        `);
        
        const mailtoLink = `mailto:natawest23@gmail.com?subject=${subject}&body=${body}`;
        window.location.href = mailtoLink;
        
        setTimeout(() => {
            console.log('Form submitted:', data);
            resolve();
        }, 1000);
    });
}

// Close modal function
function closeModal() {
    applicationModal.style.display = 'none';
    doneModal.style.display = 'none';
    document.body.style.overflow = 'auto';
}